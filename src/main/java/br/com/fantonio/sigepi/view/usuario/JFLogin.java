     private void logar() {
        String senha = ptxtSenha.getText();
        boolean loginValido = false;
        String inLogin = txtLogin.getText();
        String inSenha = ptxtSenha.getText();
        Pessoa usuarioLogado = null;
        
        for (int k = 0; k < pessoas.size(); k++) {
            if (inLogin.equals(pessoas.get(k).getLogin())) {
                if (ptxtSenha.getText().equals(pessoas.get(k).getSenha())) {
                    usuarioLogado = pessoas.get(k);
                    loginValido = true;
                    break;
                }
            }
        }     
        
        if (loginValido == false) {
            JOptionPane.showMessageDialog(null, "Usu치rio ou senha inv치lido!\n" +
                                                "Verifique se digitou seus dados corretamente ou contate o Administrador do Sistema");
            limparCampos();
        }
        else {
            if (usuarioLogado.isAtivo()) {
                loginPorTipo(usuarioLogado);
            } else {
                JOptionPane.showMessageDialog(null, "Este usuario foi desativado!\n" +
                                                    "Caso o erro persista contate o Administrador do Sistema");
                limparCampos();
            }     
        }
    }
    
    public void limparCampos() {
        txtLogin.setText("");
        ptxtSenha.setText("");
        txtLogin.requestFocus();
    }
    
    public void loginPorTipo(Pessoa usuarioLogado) {
        int op = 0;        
        Sessao.iniciarSessao(usuarioLogado);
        
        if (usuarioLogado.getTipo().equals("ADMINISTRADOR")) {
            JFPrincipalAdmin jfAdmin = new JFPrincipalAdmin();
            jfAdmin.setVisible(true);
            this.dispose(); 
        } else {
            if (usuarioLogado.getTipo().equals("TECNICO")){
                JFPrincipalTecnico jfTecnico = new JFPrincipalTecnico();
                jfTecnico.setVisible(true);
                this.dispose();
            } else {
                JFPrincipalUsuario jfUsuario = new JFPrincipalUsuario();
                jfUsuario.setVisible(true);
                this.dispose();                
            }
        }
    }      
